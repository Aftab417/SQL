--------------------------------  SQL  Commands  Types  ----------------------------------
--  There are following types of SQL commands


--------------------------------  DDL (Data Defination Language)  ----------------------------------
--  These commands are used to Define the data schemas or design

--  CREATE
--  ALTER
--  TRUNCATE
--  DROP




--------------------------------  DML (Data Manipulation Language)  ----------------------------------
--  These commands are used to manipulate the data inside database

--  INSERT
--  UPDATE
--  DELETE





--------------------------------  DQL (Data Query Language)  ----------------------------------
--  These commands are used to quering data from database

--  SELECT



--------------------------------  DCL (Data Control Language)  ----------------------------------
--  These commands are used to control the permission to use the data

--  GRANT
--  REVOKE



--------------------------------  TCL (Transactioin Control Language)  ----------------------------------

--  COMMIT
--  ROLLBACK
--  SAVEPOINT 