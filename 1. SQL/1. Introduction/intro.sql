-------------------------  Database  ----------------------------
-- A database is organized collection of structured information stored electronically in computer system.


-------------------------  DBMS (Database Management System) ----------------------------
--  DBMS is a software to manages database, like:
--  Data storage, retrieval, update
--  Data security and backup
--  Concurrency control (multiple users accessing at at time)
--  Integrity enforcement

-- e.g:  MySQL, SQlite, SQLserver, PostgreSQL, Oracle




-------------------------  SQL (Structured Query Language) ----------------------------
--  SQL is a standard language used to manage and communicate with database.
--  creating database and tables
--  inserting new data
--  Reading data
--  Deleting data
--  Controling permission and security